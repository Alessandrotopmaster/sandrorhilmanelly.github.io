// Service Worker para funcionalidade offline
const CACHE_NAME = 'alessandrotopmaster-v1';
const urlsToCache = [
  '/',
  '/index.html',
  '/estilo-mae.css',
  '/script-mae.js',
  '/pagina-principal/index.html',
  '/Premium-Absoluta',
  '/Top-Master-Mestre-Offline'
];

// Instalação do Service Worker
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Cache aberto');
        return cache.addAll(urlsToCache);
      })
      .catch(error => {
        console.log('Erro ao adicionar ao cache:', error);
      })
  );
});

// Ativação do Service Worker
self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheName !== CACHE_NAME) {
            console.log('Cache antigo removido:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});

// Interceptação de requisições
self.addEventListener('fetch', event => {
  // Apenas GET requests
  if (event.request.method !== 'GET') {
    return;
  }

  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // Se está no cache, retorna do cache
        if (response) {
          return response;
        }

        // Se não está no cache, faz requisição à rede
        return fetch(event.request).then(response => {
          // Não cachejar respostas que não são OK
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }

          // Clona a resposta
          const responseToCache = response.clone();

          caches.open(CACHE_NAME)
            .then(cache => {
              cache.put(event.request, responseToCache);
            });

          return response;
        });
      })
      .catch(() => {
        // Se offline e não está em cache, retorna página padrão
        return new Response('Você está offline. Tente novamente quando tiver conexão.', {
          status: 503,
          statusText: 'Service Unavailable',
          headers: new Headers({
            'Content-Type': 'text/plain'
          })
        });
      })
  );
});

// Mensagens do Service Worker
self.addEventListener('message', event => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});

console.log('Service Worker carregado!');
